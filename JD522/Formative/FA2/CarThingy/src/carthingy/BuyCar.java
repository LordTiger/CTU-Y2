    private void saveData() { 
       String filename = txt_fName.getText() + " " + txt_Surname.getText() + ".txt";
       FileWriter fw;
       
       try {
           fw = new FileWriter(filename);
           
           if(rBtn_Yes.isSelected()){
               fw.write("Personal Information:  \n\n");
               fw.write("----------------------------------------- \n\n");
               fw.write("Full Name: " + txt_fName.getText() + " " + txt_Surname.getText() + "\n\n");
               fw.write("ID Number: " + txt_tID.getText() + "\n\n");
               fw.write("Gender: " + cmb_Gender.getSelectedItem() + "\n\n");
               fw.write("Occupation: " + txt_Occupation.getText() + "\n\n");
               fw.write("Home Address: " + txt_Home.getText() + "\n\n");
               fw.write("Work Address: " + txt_WorkA.getText() + "\n\n");
               fw.write("Gross Pay: " + txt_GPay.getText() + "\n\n");
               fw.write("Net Pay: " + txt_NPay.getText() + "\n\n");
               fw.write("Valid License: " + "Yes" + "\n\n");
               fw.write("Model: " + cmb_Vehicles.getSelectedItem() + "\n\n");
               fw.write("Net Pay: " + txt_NPay.getText() + "\n\n");
           
               fw.write("Next of Kin Information:  \n\n");
               fw.write("----------------------------------------- \n\n");
               fw.write("Full Names: " + txt_kinfName.getText() + " " + txt_KinSurname.getText() +  "\n\n");
               fw.write("Cell Number: " + txt_KinCell.getText() + "\n\n");
               fw.write("Relationship: " + txt_Relationship.getText() + "\n\n");
               fw.close();
               HomePage home = new HomePage();
               this.setVisible(false);
               home.setVisible(true);
           }else {
               HomePage home = new HomePage();
               this.setVisible(false);
               home.setVisible(true);
           }

       } catch (Exception ex) {
           ex.printStackTrace();
       }
 
        
    }
