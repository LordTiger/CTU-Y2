    private void btn_IDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_IDActionPerformed
 
        // ID BUTTON SAVE location AND DISABLE FROM EDITING TO TXT_ID
        JFileChooser chooser = new JFileChooser(new File(System.getProperty("user.home") + "\\Documents")); //Downloads Directory as default
        chooser.setDialogTitle("Select ID");
        chooser.setAcceptAllFileFilterUsed(true);
        
        if (chooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION)
        { 
            String fileID = chooser.getSelectedFile().getPath();
            txt_ID.setText(fileID);
            txt_ID.setEditable(false);
        }
        
    }//GEN-LAST:event_btn_IDActionPerformed

